function validarFormulario() {
    var valor = true;
    if ($("#nombre_ubicacion").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vacio.");
        $("#nombre_ubicacion").focus();
    }
    return valor;
}

function limpiarFormulario() {
    $("#id_ubicacion").val("0");
    $("#nombre_ubicacion").val("");
}
