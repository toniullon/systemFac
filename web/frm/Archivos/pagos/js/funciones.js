function validarFormulario() {
    var valor = true;
    if ($("#forma_pago").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Forma de pago no puede estar vacio.");
        $("#forma_pago").focus();
    }
    return valor;
}
function limpiarFormulario(){
    $("#id_pago").val("0");
    $("#forma_pago").val("");
}